/**
 * Get latest blog posts
 * @param limit Maximum number of posts to return
 * @returns Array of latest blog posts
 */
export const getLatestBlogPosts = (limit: number = 5): BlogPost[] => {
  return [...blogPosts]
    .sort((a, b) => b.publishedAt.getTime() - a.publishedAt.getTime())
    .slice(0, limit);
};

/**
 * Get related blog posts based on a post ID
 * First tries to find posts with matching tags,
 * then fills with recent posts if needed
 * 
 * @param postId ID of the current post
 * @param limit Maximum number of posts to return
 * @returns Array of related blog posts
 */
export const getRelatedPosts = (postId: string, limit: number = 3): BlogPost[] => {
  const currentPost = blogPosts.find(post => post.id === postId);
  
  if (!currentPost) {
    return [];
  }
  
  // Find posts with shared tags, excluding the current post
  const relatedByTags = blogPosts.filter(post => 
    post.id !== postId && 
    post.tags.some(tag => currentPost.tags.includes(tag))
  );
  
  // Sort by number of matching tags (most matches first)
  relatedByTags.sort((a, b) => {
    const aMatches = a.tags.filter(tag => currentPost.tags.includes(tag)).length;
    const bMatches = b.tags.filter(tag => currentPost.tags.includes(tag)).length;
    return bMatches - aMatches;
  });
  
  // If we have enough related posts by tags, return them
  if (relatedByTags.length >= limit) {
    return relatedByTags.slice(0, limit);
  }
  
  // Otherwise, fill with recent posts (excluding the current post and already selected related posts)
  const relatedIds = new Set(relatedByTags.map(post => post.id));
  const recentPosts = blogPosts
    .filter(post => post.id !== postId && !relatedIds.has(post.id))
    .sort((a, b) => b.publishedAt.getTime() - a.publishedAt.getTime());
  
  return [...relatedByTags, ...recentPosts].slice(0, limit);
};